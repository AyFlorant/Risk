package Risk;

public class Unite {

    protected int type;
    protected int cost;
    protected int[] power;
    protected int ATT_prio;
    protected int DEF_prio;
    protected int MVT_tour;


    public int getType() {
        return type;
    }

    public int getCost() {
        return cost;
    }

    public int[] getPower() {
        return power;
    }

    public int getATT_prio() {
        return ATT_prio;
    }

    public int getDEF_prio() {
        return DEF_prio;
    }

    public int getMVT_tour() {
        return MVT_tour;
    }
}


